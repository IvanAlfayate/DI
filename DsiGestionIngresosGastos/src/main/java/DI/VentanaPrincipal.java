    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked

        Error.setText("");  //Borro el mensaje de error
        //Comprobamos si hay una fila seleccionada
        int fila;
        fila = jTable1.getSelectedRow();
        if (fila == -1) { //si no hay una fila seleccionada
            Error.setText("Selecciona una fila");
        } else {  //si la fila esta seleccionada la borramos de si datatable
            IngresoTableModel ctm = (IngresoTableModel) jTable1.getModel(); //recuperamos su TableModel
            ctm.deleteRow(fila);
        }


    }//GEN-LAST:event_jMenu2MouseClicked
    private void refrescaTabla() {
        IngresoTableModel ctm = (IngresoTableModel) jTable1.getModel();  //recupero el TableModel de la tabla
        ctm.refrescar();  //lanzo su metodo para refrescarlo
    }

